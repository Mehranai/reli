def module_has_nonzero_gradient(
    module: nn.Module,
) -> bool:
    for parameter in module.parameters():
        if (
            parameter.requires_grad
            and parameter.grad is not None
            and torch.isfinite(
                parameter.grad
            ).all()
            and parameter.grad.abs().sum() > 0
        ):
            return True

    return False


