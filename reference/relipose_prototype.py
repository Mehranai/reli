import torchvision.transforms.functional as TF


class HICOImageTransform:
    """
    Resize an image while preserving aspect ratio, pad it to a
    square canvas, optionally flip it and normalize it.
    """

    def __init__(
        self,
        output_size: int,
        training: bool,
        horizontal_flip_probability: float = 0.5,
    ) -> None:
        self.output_size = output_size
        self.training = training

        self.horizontal_flip_probability = (
            horizontal_flip_probability
        )

        self.mean = [
            0.485,
            0.456,
            0.406,
        ]

        self.std = [
            0.229,
            0.224,
            0.225,
        ]

    @staticmethod
    def resize_boxes(
        boxes: Tensor,
        scale: float,
    ) -> Tensor:
        if boxes.numel() == 0:
            return boxes

        return boxes * scale

    @staticmethod
    def flip_boxes_horizontally(
        boxes: Tensor,
        image_width: int,
    ) -> Tensor:
        if boxes.numel() == 0:
            return boxes

        flipped_boxes = boxes.clone()

        old_x1 = boxes[:, 0].clone()
        old_x2 = boxes[:, 2].clone()

        flipped_boxes[:, 0] = (
            image_width - old_x2
        )

        flipped_boxes[:, 2] = (
            image_width - old_x1
        )

        return flipped_boxes

    def __call__(
        self,
        image: Image.Image,
        target: Dict[str, Tensor],
    ) -> tuple[Tensor, Dict[str, Tensor]]:

        if image.mode != "RGB":
            image = image.convert("RGB")

        original_width, original_height = image.size

        longest_side = max(
            original_height,
            original_width,
        )

        scale = (
            self.output_size / longest_side
        )

        resized_height = max(
            1,
            int(round(original_height * scale)),
        )

        resized_width = max(
            1,
            int(round(original_width * scale)),
        )

        image = TF.resize(
            image,
            size=[
                resized_height,
                resized_width,
            ],
            antialias=True,
        )

        target = {
            key: value.clone()
            if isinstance(value, Tensor)
            else value
            for key, value in target.items()
        }

        target["human_boxes"] = self.resize_boxes(
            target["human_boxes"],
            scale,
        )

        target["object_boxes"] = self.resize_boxes(
            target["object_boxes"],
            scale,
        )

        should_flip = (
            self.training
            and random.random()
            < self.horizontal_flip_probability
        )

        if should_flip:
            image = TF.hflip(image)

            target["human_boxes"] = (
                self.flip_boxes_horizontally(
                    target["human_boxes"],
                    resized_width,
                )
            )

            target["object_boxes"] = (
                self.flip_boxes_horizontally(
                    target["object_boxes"],
                    resized_width,
                )
            )

        padding_right = (
            self.output_size - resized_width
        )

        padding_bottom = (
            self.output_size - resized_height
        )

        image = TF.pad(
            image,
            padding=[
                0,
                0,
                padding_right,
                padding_bottom,
            ],
            fill=0,
        )

        image = TF.to_tensor(image)

        image = TF.normalize(
            image,
            mean=self.mean,
            std=self.std,
        )

        target["human_boxes"] = clamp_boxes_to_image(
            target["human_boxes"],
            image_height=self.output_size,
            image_width=self.output_size,
        )

        target["object_boxes"] = clamp_boxes_to_image(
            target["object_boxes"],
            image_height=self.output_size,
            image_width=self.output_size,
        )

        target["original_size"] = torch.tensor(
            [
                original_height,
                original_width,
            ],
            dtype=torch.long,
        )

        target["resized_size"] = torch.tensor(
            [
                resized_height,
                resized_width,
            ],
            dtype=torch.long,
        )

        target["input_size"] = torch.tensor(
            [
                self.output_size,
                self.output_size,
            ],
            dtype=torch.long,
        )

        target["resize_scale"] = torch.tensor(
            scale,
            dtype=torch.float32,
        )

        target["was_flipped"] = torch.tensor(
            should_flip,
            dtype=torch.bool,
        )

        return image, target

