class PoseQualityGuidedSparseRefiner(nn.Module):
    """
    Refine uncertain joints using local Human RoI features.

    Training:
        Soft differentiable gate over all joints.

    Evaluation:
        Optionally refine only Top-K lowest-quality joints.

    The quality score rho is not the final Joint Reliability.
    """

    def __init__(
        self,
        feature_channels: int,
        model_dim: int,
        num_joints: int,
        patch_size: int,
        patch_radius: float,
        quality_threshold: float,
        temperature: float,
        maximum_coordinate_offset: float,
        maximum_log_uncertainty_update: float,
        inference_topk: int,
        hard_topk_in_eval: bool,
        minimum_uncertainty: float,
        dropout: float,
    ) -> None:
        super().__init__()

        if temperature <= 0:
            raise ValueError(
                "temperature must be positive."
            )

        if maximum_coordinate_offset <= 0:
            raise ValueError(
                "maximum_coordinate_offset must be positive."
            )

        if not 0 <= inference_topk <= num_joints:
            raise ValueError(
                "inference_topk must be between "
                "zero and num_joints."
            )

        self.model_dim = model_dim
        self.num_joints = num_joints

        self.quality_threshold = (
            quality_threshold
        )

        self.temperature = temperature

        self.maximum_coordinate_offset = (
            maximum_coordinate_offset
        )

        self.maximum_log_uncertainty_update = (
            maximum_log_uncertainty_update
        )

        self.inference_topk = inference_topk
        self.hard_topk_in_eval = hard_topk_in_eval

        self.minimum_uncertainty = (
            minimum_uncertainty
        )

        self.patch_sampler = (
            LocalJointPatchSampler(
                patch_size=patch_size,
                radius=patch_radius,
                padding_mode="border",
                align_corners=False,
            )
        )

        self.patch_encoder = (
            LocalJointPatchEncoder(
                input_channels=feature_channels,
                model_dim=model_dim,
                dropout=dropout,
            )
        )

        # token + local feature + xy + confidence
        # + uncertainty + coarse quality
        refinement_input_dim = (
            model_dim
            + model_dim
            + 2
            + 1
            + 1
            + 1
        )

        self.refinement_fusion = nn.Sequential(
            nn.Linear(
                refinement_input_dim,
                model_dim,
            ),
            nn.LayerNorm(model_dim),
            nn.GELU(),
            nn.Dropout(dropout),

            nn.Linear(
                model_dim,
                model_dim,
            ),
            nn.GELU(),
            nn.Dropout(dropout),
        )

        self.coordinate_offset_head = nn.Linear(
            model_dim,
            2,
        )

        self.token_delta_head = nn.Linear(
            model_dim,
            model_dim,
        )

        self.confidence_delta_head = nn.Linear(
            model_dim,
            1,
        )

        self.uncertainty_delta_head = nn.Linear(
            model_dim,
            1,
        )

        self.final_token_norm = nn.LayerNorm(
            model_dim
        )

        self._reset_output_heads()

    def _reset_output_heads(self) -> None:
        """
        Start refinement as an identity transformation.
        """

        output_heads = [
            self.coordinate_offset_head,
            self.token_delta_head,
            self.confidence_delta_head,
            self.uncertainty_delta_head,
        ]

        for head in output_heads:
            nn.init.zeros_(head.weight)
            nn.init.zeros_(head.bias)

    def _soft_refinement_gate(
        self,
        coarse_quality: Tensor,
    ) -> Tensor:
        """
        Lower quality -> larger refinement gate.
        """

        return torch.sigmoid(
            (
                self.quality_threshold
                - coarse_quality
            )
            / self.temperature
        )

    @staticmethod
    def _gather_vectors(
        tensor: Tensor,
        indices: Tensor,
    ) -> Tensor:
        """
        Gather [N, K, D] using [N, M] indices.
        """

        expanded_indices = indices.unsqueeze(
            -1
        ).expand(
            -1,
            -1,
            tensor.shape[-1],
        )

        return torch.gather(
            tensor,
            dim=1,
            index=expanded_indices,
        )

    @staticmethod
    def _gather_scalars(
        tensor: Tensor,
        indices: Tensor,
    ) -> Tensor:
        """
        Gather [N, K] using [N, M] indices.
        """

        return torch.gather(
            tensor,
            dim=1,
            index=indices,
        )

    def _compute_refinement_candidates(
        self,
        human_roi_features: Tensor,
        joint_tokens: Tensor,
        joint_coordinates: Tensor,
        joint_confidence: Tensor,
        joint_uncertainty: Tensor,
        coarse_quality: Tensor,
    ) -> Dict[str, Tensor]:
        """
        Compute refinement predictions for M selected joints.
        """

        local_patches = self.patch_sampler(
            human_roi_features=(
                human_roi_features
            ),
            joint_coordinates=(
                joint_coordinates
            ),
        )

        local_joint_features = (
            self.patch_encoder(
                local_patches
            )
        )

        refinement_input = torch.cat(
            [
                joint_tokens,
                local_joint_features,
                joint_coordinates,
                joint_confidence.unsqueeze(-1),
                joint_uncertainty.unsqueeze(-1),
                coarse_quality.unsqueeze(-1),
            ],
            dim=-1,
        )

        hidden_features = self.refinement_fusion(
            refinement_input
        )

        coordinate_offset = torch.tanh(
            self.coordinate_offset_head(
                hidden_features
            )
        )

        coordinate_offset = (
            coordinate_offset
            * self.maximum_coordinate_offset
        )

        token_delta = self.token_delta_head(
            hidden_features
        )

        confidence_delta = (
            self.confidence_delta_head(
                hidden_features
            ).squeeze(-1)
        )

        uncertainty_delta = torch.tanh(
            self.uncertainty_delta_head(
                hidden_features
            ).squeeze(-1)
        )

        uncertainty_delta = (
            uncertainty_delta
            * self.maximum_log_uncertainty_update
        )

        safe_confidence = joint_confidence.clamp(
            min=1e-4,
            max=1.0 - 1e-4,
        )

        confidence_logits = torch.logit(
            safe_confidence
        )

        candidate_confidence = torch.sigmoid(
            confidence_logits
            + confidence_delta
        )

        candidate_uncertainty = (
            joint_uncertainty
            * torch.exp(
                uncertainty_delta
            )
        ).clamp_min(
            self.minimum_uncertainty
        )

        return {
            "local_joint_features": (
                local_joint_features
            ),
            "coordinate_offset": (
                coordinate_offset
            ),
            "token_delta": token_delta,
            "candidate_confidence": (
                candidate_confidence
            ),
            "candidate_uncertainty": (
                candidate_uncertainty
            ),
        }

    def _soft_forward(
        self,
        human_roi_features: Tensor,
        joint_tokens: Tensor,
        joint_coordinates: Tensor,
        joint_confidence: Tensor,
        joint_uncertainty: Tensor,
        coarse_quality: Tensor,
    ) -> Dict[str, Tensor]:

        candidates = (
            self._compute_refinement_candidates(
                human_roi_features=(
                    human_roi_features
                ),
                joint_tokens=joint_tokens,
                joint_coordinates=(
                    joint_coordinates
                ),
                joint_confidence=(
                    joint_confidence
                ),
                joint_uncertainty=(
                    joint_uncertainty
                ),
                coarse_quality=coarse_quality,
            )
        )

        refinement_gate = (
            self._soft_refinement_gate(
                coarse_quality
            )
        )

        vector_gate = refinement_gate.unsqueeze(
            -1
        )

        refined_coordinates = (
            joint_coordinates
            + vector_gate
            * candidates["coordinate_offset"]
        ).clamp(
            min=0.0,
            max=1.0,
        )

        refined_tokens = self.final_token_norm(
            joint_tokens
            + vector_gate
            * candidates["token_delta"]
        )

        refined_confidence = (
            joint_confidence
            + refinement_gate
            * (
                candidates[
                    "candidate_confidence"
                ]
                - joint_confidence
            )
        )

        refined_uncertainty = (
            joint_uncertainty
            + refinement_gate
            * (
                candidates[
                    "candidate_uncertainty"
                ]
                - joint_uncertainty
            )
        ).clamp_min(
            self.minimum_uncertainty
        )

        refinement_mask = (
            refinement_gate >= 0.5
        )

        return {
            "joint_tokens": refined_tokens,
            "joint_coordinates": (
                refined_coordinates
            ),
            "joint_confidence": (
                refined_confidence
            ),
            "joint_uncertainty": (
                refined_uncertainty
            ),
            "local_joint_features": (
                candidates[
                    "local_joint_features"
                ]
            ),
            "refinement_gate": (
                refinement_gate
            ),
            "refinement_mask": (
                refinement_mask
            ),
        }

    def _hard_topk_forward(
        self,
        human_roi_features: Tensor,
        joint_tokens: Tensor,
        joint_coordinates: Tensor,
        joint_confidence: Tensor,
        joint_uncertainty: Tensor,
        coarse_quality: Tensor,
    ) -> Dict[str, Tensor]:
        """
        Refine only the Top-K lowest-quality joints.
        """

        number_of_humans = (
            joint_tokens.shape[0]
        )

        number_to_refine = min(
            self.inference_topk,
            self.num_joints,
        )

        if number_to_refine == 0:
            return {
                "joint_tokens": joint_tokens,
                "joint_coordinates": (
                    joint_coordinates
                ),
                "joint_confidence": (
                    joint_confidence
                ),
                "joint_uncertainty": (
                    joint_uncertainty
                ),
                "local_joint_features": (
                    torch.zeros_like(
                        joint_tokens
                    )
                ),
                "refinement_gate": (
                    torch.zeros_like(
                        coarse_quality
                    )
                ),
                "refinement_mask": (
                    torch.zeros_like(
                        coarse_quality,
                        dtype=torch.bool,
                    )
                ),
            }

        # Smallest quality values are selected.
        selected_indices = torch.topk(
            coarse_quality,
            k=number_to_refine,
            dim=1,
            largest=False,
            sorted=False,
        ).indices

        selected_tokens = self._gather_vectors(
            joint_tokens,
            selected_indices,
        )

        selected_coordinates = (
            self._gather_vectors(
                joint_coordinates,
                selected_indices,
            )
        )

        selected_confidence = (
            self._gather_scalars(
                joint_confidence,
                selected_indices,
            )
        )

        selected_uncertainty = (
            self._gather_scalars(
                joint_uncertainty,
                selected_indices,
            )
        )

        selected_quality = (
            self._gather_scalars(
                coarse_quality,
                selected_indices,
            )
        )

        candidates = (
            self._compute_refinement_candidates(
                human_roi_features=(
                    human_roi_features
                ),
                joint_tokens=selected_tokens,
                joint_coordinates=(
                    selected_coordinates
                ),
                joint_confidence=(
                    selected_confidence
                ),
                joint_uncertainty=(
                    selected_uncertainty
                ),
                coarse_quality=(
                    selected_quality
                ),
            )
        )

        coordinate_index = (
            selected_indices
            .unsqueeze(-1)
            .expand(-1, -1, 2)
        )

        token_index = (
            selected_indices
            .unsqueeze(-1)
            .expand(
                -1,
                -1,
                self.model_dim,
            )
        )

        coordinate_update = torch.zeros_like(
            joint_coordinates
        ).scatter(
            dim=1,
            index=coordinate_index,
            src=candidates[
                "coordinate_offset"
            ],
        )

        token_update = torch.zeros_like(
            joint_tokens
        ).scatter(
            dim=1,
            index=token_index,
            src=candidates[
                "token_delta"
            ],
        )

        confidence_change = (
            candidates[
                "candidate_confidence"
            ]
            - selected_confidence
        )

        confidence_update = torch.zeros_like(
            joint_confidence
        ).scatter(
            dim=1,
            index=selected_indices,
            src=confidence_change,
        )

        uncertainty_change = (
            candidates[
                "candidate_uncertainty"
            ]
            - selected_uncertainty
        )

        uncertainty_update = torch.zeros_like(
            joint_uncertainty
        ).scatter(
            dim=1,
            index=selected_indices,
            src=uncertainty_change,
        )

        local_feature_update = torch.zeros_like(
            joint_tokens
        ).scatter(
            dim=1,
            index=token_index,
            src=candidates[
                "local_joint_features"
            ],
        )

        refinement_gate = torch.zeros_like(
            coarse_quality
        ).scatter(
            dim=1,
            index=selected_indices,
            src=torch.ones(
                (
                    number_of_humans,
                    number_to_refine,
                ),
                dtype=coarse_quality.dtype,
                device=coarse_quality.device,
            ),
        )

        refined_coordinates = (
            joint_coordinates
            + coordinate_update
        ).clamp(
            min=0.0,
            max=1.0,
        )

        refined_tokens = self.final_token_norm(
            joint_tokens
            + token_update
        )

        refined_confidence = (
            joint_confidence
            + confidence_update
        ).clamp(
            min=0.0,
            max=1.0,
        )

        refined_uncertainty = (
            joint_uncertainty
            + uncertainty_update
        ).clamp_min(
            self.minimum_uncertainty
        )

        return {
            "joint_tokens": refined_tokens,
            "joint_coordinates": (
                refined_coordinates
            ),
            "joint_confidence": (
                refined_confidence
            ),
            "joint_uncertainty": (
                refined_uncertainty
            ),
            "local_joint_features": (
                local_feature_update
            ),
            "refinement_gate": (
                refinement_gate
            ),
            "refinement_mask": (
                refinement_gate.bool()
            ),
        }

    def forward(
        self,
        human_roi_features: Tensor,
        coarse_pose_outputs: Dict[str, Tensor],
    ) -> Dict[str, Tensor]:

        required_keys = {
            "joint_tokens_initial",
            "joint_coordinates_coarse",
            "joint_confidence",
            "joint_uncertainty",
            "coarse_quality",
        }

        missing_keys = (
            required_keys
            - coarse_pose_outputs.keys()
        )

        if missing_keys:
            raise KeyError(
                f"Missing coarse pose outputs: "
                f"{sorted(missing_keys)}"
            )

        joint_tokens = coarse_pose_outputs[
            "joint_tokens_initial"
        ]

        joint_coordinates = (
            coarse_pose_outputs[
                "joint_coordinates_coarse"
            ]
        )

        joint_confidence = (
            coarse_pose_outputs[
                "joint_confidence"
            ]
        )

        joint_uncertainty = (
            coarse_pose_outputs[
                "joint_uncertainty"
            ]
        )

        coarse_quality = (
            coarse_pose_outputs[
                "coarse_quality"
            ]
        )

        number_of_humans = joint_tokens.shape[0]

        if number_of_humans == 0:
            return {
                "joint_tokens": joint_tokens,
                "joint_coordinates": (
                    joint_coordinates
                ),
                "joint_confidence": (
                    joint_confidence
                ),
                "joint_uncertainty": (
                    joint_uncertainty
                ),
                "local_joint_features": (
                    torch.empty_like(
                        joint_tokens
                    )
                ),
                "refinement_gate": (
                    torch.empty_like(
                        coarse_quality
                    )
                ),
                "refinement_mask": (
                    torch.empty_like(
                        coarse_quality,
                        dtype=torch.bool,
                    )
                ),
            }

        if (
            not self.training
            and self.hard_topk_in_eval
        ):
            return self._hard_topk_forward(
                human_roi_features=(
                    human_roi_features
                ),
                joint_tokens=joint_tokens,
                joint_coordinates=(
                    joint_coordinates
                ),
                joint_confidence=(
                    joint_confidence
                ),
                joint_uncertainty=(
                    joint_uncertainty
                ),
                coarse_quality=coarse_quality,
            )

        return self._soft_forward(
            human_roi_features=(
                human_roi_features
            ),
            joint_tokens=joint_tokens,
            joint_coordinates=(
                joint_coordinates
            ),
            joint_confidence=(
                joint_confidence
            ),
            joint_uncertainty=(
                joint_uncertainty
            ),
            coarse_quality=coarse_quality,
        )

