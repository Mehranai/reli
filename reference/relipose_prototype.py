# 55. Local Joint Patch Encoder
# ============================================================

class LocalJointPatchEncoder(nn.Module):
    """
    Encode local joint patches into feature vectors.

    Input:
        patches:
            [N_H, M, C, P, P]

    Output:
        local_joint_features:
            [N_H, M, D]
    """

    def __init__(
        self,
        input_channels: int,
        model_dim: int,
        dropout: float,
    ) -> None:
        super().__init__()

        if input_channels <= 0:
            raise ValueError(
                "input_channels must be positive."
            )

        if model_dim <= 0:
            raise ValueError(
                "model_dim must be positive."
            )

        number_of_groups = 32

        while (
            number_of_groups > 1
            and model_dim % number_of_groups != 0
        ):
            number_of_groups //= 2

        self.input_channels = input_channels
        self.model_dim = model_dim

        self.encoder = nn.Sequential(
            nn.Conv2d(
                input_channels,
                model_dim,
                kernel_size=1,
                bias=False,
            ),
            nn.GroupNorm(
                number_of_groups,
                model_dim,
            ),
            nn.GELU(),

            nn.Conv2d(
                model_dim,
                model_dim,
                kernel_size=3,
                padding=1,
                bias=False,
            ),
            nn.GroupNorm(
                number_of_groups,
                model_dim,
            ),
            nn.GELU(),

            nn.Dropout2d(dropout),

            nn.AdaptiveAvgPool2d(
                output_size=1,
            ),
        )

    def forward(
        self,
        patches: Tensor,
    ) -> Tensor:

        if patches.ndim != 5:
            raise ValueError(
                "patches must have shape "
                "[N_H, M, C, P, P]."
            )

        number_of_humans = patches.shape[0]
        number_of_joints = patches.shape[1]
        channels = patches.shape[2]
        patch_height = patches.shape[3]
        patch_width = patches.shape[4]

        if channels != self.input_channels:
            raise ValueError(
                f"Expected {self.input_channels} channels, "
                f"received {channels}."
            )

        if (
            number_of_humans == 0
            or number_of_joints == 0
        ):
            return torch.empty(
                (
                    number_of_humans,
                    number_of_joints,
                    self.model_dim,
                ),
                dtype=patches.dtype,
                device=patches.device,
            )

        flattened_patches = patches.reshape(
            number_of_humans
            * number_of_joints,
            channels,
            patch_height,
            patch_width,
        )

        encoded = self.encoder(
            flattened_patches
        )

        encoded = encoded.flatten(
            start_dim=1
        )

        encoded = encoded.reshape(
            number_of_humans,
            number_of_joints,
            self.model_dim,
        )

        return encoded

